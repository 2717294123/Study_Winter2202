package wangyf.operator;

public class OperatorDemo6 {
    public static void main(String[] args) {
        //目标：学会使用关系运算符
        int a = 10;
        int b = 10;
        boolean re = a == b;
        System.out.println(re);

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a <= b);
        System.out.println(a >= b);
    }
}
