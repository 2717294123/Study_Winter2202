package wangyf.operator;

public class OperatorDemo1 {
    public static void main(String[] args) {
        //掌握基本运算符使用：+ - * / %
        int a = 10;
        int b = 3;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);//3.3333333  ===>  3
        System.out.println(a * 1.0/ b);
        System.out.println(a % b);
    }
}
